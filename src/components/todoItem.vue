<template>
<div>
  <div v-if="editing" class="edit-todo list-group-item">
    <div class="input-group">
      <input id="edit-todo" class="col6" type="text" v-model="text">
      <div class="btn-group float-right">
        <button
          v-on:click="saveToDo()"
          class="btn btn-success"
          id="save-todo"
        >
          save ToDo
        </button>
      </div>
    </div>
  </div>
  <div class="list-group-item" v-else>
    <span class="todo-text">
      {{text}}
    </span>
    <div class="btn-group btn-group-sm float-right" role="group">
      <button class="btn btn-warning todo-edit" v-on:click="editToDo()">edit</button>
      <button class="btn btn-danger todo-delete" v-on:click="deleteToDo()">delete</button>
    </div>
  </div>
</div>
</template>

<script>
export default {
  props: ['todo', 'todoindex'],
  data () {
  return {
    editing: false,
    text: this.todo.text,
    id: this.todo.id
  }
},
  methods: {
    saveToDo () {
      this.editing = false
    },
    editToDo () {
      this.editing = true
    },
    deleteToDo () {
      this.$emit('delete-todo', this.todoindex)
    }
  }
}
</script>

<style>
  .input-group {
    width: 100%;
  }
</style>
